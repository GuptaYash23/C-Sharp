/* GUID

A Guid is a "globally unique identifier".

We can use Guid to enforce uniqueness.

Guid by default is of type struct.

This means value will not conflict with another Guid.

We find that databased can use Guids to provide a unique identifier for a row of data.

A Guid is a 128-bit (16 bytes) that you can use across all computers and networks wherever a unique identifier is required.

It has 36 characters including hyphens (32 characters without hypens).

